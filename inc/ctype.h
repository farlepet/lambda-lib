#ifndef LAMBDA_LIB_CTYPE_H
#define LAMBDA_LIB_CTYPE_H

/**
 * @brief Check if character is a letter or a number
 *
 * @param c Character to check
 * @return int 1 if characer is letter or number, else 0
 */
int isalnum(int c);

/**
 * @brief Check if character is whitespace
 *
 * @param c Character to check
 * @return int 1 if character is whitespace, else 0
 */
int isspace(int c);

#endif
